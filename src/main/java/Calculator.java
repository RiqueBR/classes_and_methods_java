public class Calculator {

    public int getSum(int num1, int num2){
        return num1 + num2;
    }

    public int getSubtraction(int num1, int num2){
        return num1 - num2;
    }

    public int getMultiplication(int num1, int num2){
        return num1 * num2;
    }

    public int getDivision(int num1, int num2){
        return num1 / num2;
    }
}
